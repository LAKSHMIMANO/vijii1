#include <stdio.h>

int main() 
{
  int n;
  
  printf("enter the number from 1 to 10");
  scanf("%d",&n);
  switch(n)
  {
    case1:
    printf("one");
    break;
    case2:
    printf("two");
    break;
    case3:
    printf("three");
    break;
    case4:
    printf("four");
    break;
    case5:
    printf("five");
    break;
    case6:
    printf("six");
    break;
    case7:
    printf("seven");
    break;
    case8:
    printf("eight");
    break;
    case9:
    printf("nine");
    break;
    case10:
    printf("ten");
    break;
    default:
    printf("no such number");
  }
  return 0;
}
